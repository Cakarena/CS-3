##Given a string, return a version without the first and last char,
## so "Hello" yields "ell". The string length will be at least 2.
def without_end(str):
	print (str[1:-1])

without_end("hello")
without_end("java")
without_end("coding")
without_end("code")
without_end("ab")
without_end("chocolate")
without_end("kitten")
without_end("woohoo")

